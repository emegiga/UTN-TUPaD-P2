public class Contribuyente {
    private String nombre;
    private int cuil;

    // constructor
    public Contribuyente(String nombre, int cuil) {
        this.nombre = nombre;
        this.cuil = cuil;
    }

    // getters
    public String getNombre() {
        return nombre;
    }

    public int getCuil() {
        return cuil;
    }
    
    
}
